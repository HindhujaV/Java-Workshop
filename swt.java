public class swt {
    public static void main(String[] args) {
        int day=1;
        switch(day)
        {
            case 1:
            {
                System.out.println("monday");
                break;
            }
            case 2:
            {
                System.out.println("tuesday");
                break;
            }
            case 3:
            {
                System.out.println("wednesday");
                break;
            }
            default:
            {
                System.out.println("not a week");
            }
        }
    }
}
